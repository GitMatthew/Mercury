package com.corso.model;

public class Ente {
	
	private int id_ente = 0 ;
	private String nome_ente = "" ;
	private String psw_ente = "" ;
	private String telefono_ente = "" ;
	private int id_status = 0 ;
	private String email_ente = "" ;
	private String user_ente = "" ;
	private String url_img_ente = "" ;
	private String descrizione_ente = "" ;
	private String url_sito_ente = "" ;
	
	public Ente () {}

	public int getId_ente() {
		return id_ente;
	}

	public void setId_ente(int id_ente) {
		this.id_ente = id_ente;
	}

	public String getNome_ente() {
		return nome_ente;
	}

	public void setNome_ente(String nome_ente) {
		this.nome_ente = nome_ente;
	}

	public String getPsw_ente() {
		return psw_ente;
	}

	public void setPsw_ente(String psw_ente) {
		this.psw_ente = psw_ente;
	}

	public String getTelefono_ente() {
		return telefono_ente;
	}

	public void setTelefono_ente(String telefono_ente) {
		this.telefono_ente = telefono_ente;
	}

	public int getId_status() {
		return id_status;
	}

	public void setId_status(int id_status) {
		this.id_status = id_status;
	}

	public String getEmail_ente() {
		return email_ente;
	}

	public void setEmail_ente(String email_ente) {
		this.email_ente = email_ente;
	}

	public String getUser_ente() {
		return user_ente;
	}

	public void setUser_ente(String user_ente) {
		this.user_ente = user_ente;
	}

	public String getUrl_img_ente() {
		return url_img_ente;
	}

	public void setUrl_img_ente(String url_img_ente) {
		this.url_img_ente = url_img_ente;
	}

	public String getDescrizione_ente() {
		return descrizione_ente;
	}

	public void setDescrizione_ente(String descrizione_ente) {
		this.descrizione_ente = descrizione_ente;
	}

	public String getUrl_sito_ente() {
		return url_sito_ente;
	}

	public void setUrl_sito_ente(String url_sito_ente) {
		this.url_sito_ente = url_sito_ente;
	}
	
	
	

}
